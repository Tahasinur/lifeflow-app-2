package com.lifeflow.backend;

import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;
import org.springframework.boot.CommandLineRunner;
import org.springframework.context.annotation.Bean;
import com.lifeflow.backend.repository.UserRepository;
import com.lifeflow.backend.util.AdminUserInitializer;

@SpringBootApplication
public class BackendApplication {

	public static void main(String[] args) {
		SpringApplication.run(BackendApplication.class, args);
	}

	@Bean
	public CommandLineRunner initializeAdminUser(UserRepository userRepository) {
		return args -> {
			AdminUserInitializer.createAdminUser(userRepository);
		};
	}

}
